# Kết quả của phép so sánh chỉ có 2 giá trị: True, False 
# Kiểu dữ liệu Boolean (True, False )

print("is 5 equal 5:" ,5 == 5) #True
print("is 5 equal 6:" ,5 == 6) #False
print("is 5 greater than 3:", 5 > 3) #True
print("is 3 greater than 4:", 3 > 4) #False
print("is 3 less than 5:", 3 < 5) #True

print("is 5 greater or equal to 4:", 5 >= 4) #True
print("is 5 greater or equal to 5:", 5 >= 5) #True

